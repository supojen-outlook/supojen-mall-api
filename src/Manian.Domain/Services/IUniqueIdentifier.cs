namespace Manian.Domain.Services;

/// <summary>
/// 唯一識別碼產生器介面
/// 
/// 這個介面定義了在分散式系統中產生唯一識別碼的契約
/// 遵循「相依於抽象，不相依於具體實作」的設計原則
/// 
/// 為什麼需要這個介面？
/// 1. 隔離具體實作（如 Snowflake、UUID、資料庫序號等）
/// 2. 方便單元測試（可以 Mock 固定 ID）
/// 3. 可以隨時替換 ID 產生演算法而不影響上層程式碼
/// 
/// 常見實作方式：
/// - Snowflake (Twitter) - 分散式系統常用
/// - UUID/GUID - 簡單但較長
/// - Hi/Lo 演算法 - 資料庫友善
/// - Redis 原子增量 - 需要額外基礎設施
/// </summary>
public interface IUniqueIdentifier
{
    /// <summary>
    /// 產生 32 位元整數格式的唯一識別碼
    /// 
    /// 適用場景：
    /// - 資料表主鍵為 int 型別（節省儲存空間）
    /// - 需要較短數值的識別碼（如訂單編號、使用者 ID）
    /// - 與舊系統整合時需要 int 型別
    /// 
    /// 注意事項：
    /// - int 最大值約 21 億，需評估是否足夠
    /// - 某些演算法（如 Snowflake）原生產生 64 位元，轉為 32 位元可能損失唯一性
    /// </summary>
    /// <returns>32 位元整數識別碼</returns>
    int NextInt();

    /// <summary>
    /// 產生 64 位元長整數格式的唯一識別碼
    /// 
    /// 適用場景：
    /// - 分散式系統（Snowflake 標準輸出）
    /// - 高併發、大量資料的系統
    /// - 需要絕對全域唯一的識別碼
    /// 
    /// 64 位元優點：
    /// - 容納更大數值（約 9.2×10¹⁸）
    /// - 可內含時間戳、機器 ID 等資訊（如 Snowflake）
    /// - 避免 32 位元溢位風險
    /// </summary>
    /// <returns>64 位元長整數識別碼</returns>
    long NextLong();
}