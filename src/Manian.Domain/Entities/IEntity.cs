namespace Xiao.Domain.Entities;

/// <summary>
/// 領域實體的標記介面 (Marker Interface)
/// 
/// 這個介面本身沒有任何方法或屬性，它的唯一目的是「標記」一個類別為領域實體。
/// 這是一種在程式碼中表達意圖的設計模式，稱為「標記介面模式」。
/// 
/// 為什麼需要這個介面？
/// ---------------------------------------------------------------------
/// 1. 型別識別：讓程式可以透過反射找出「哪些類別是領域實體」
/// 2. 泛型約束：可以用 where T : IEntity 限制泛型參數必須是實體
/// 3. 意圖表達：看到 class User : IEntity 就知道 User 是一個領域實體
/// 4. 架構強制：確保團隊成員用統一的方式標記實體
/// 
/// 使用場景：
/// ---------------------------------------------------------------------
/// 1. EF Core 自動註冊：掃描所有實作 IEntity 的類別，自動加入 DbContext
/// 2. 基礎倉儲：IRepository<T> where T : IEntity
/// 3. 通用邏輯：對所有實體套用共用配置（如審計欄位、軟刪除）
/// 4. 單元測試：篩選出需要測試的實體
/// 
/// 範例：
/// ---------------------------------------------------------------------
/// public class User : IEntity
/// {
///     public int Id { get; set; }
///     public string Name { get; set; }
/// }
/// 
/// public class Product : IEntity
/// {
///     public Guid Id { get; set; }
///     public string Title { get; set; }
/// }
/// 
/// 設計考量：
/// ---------------------------------------------------------------------
/// - 為什麼用介面而不是基礎類別？
///   C# 只能單一繼承，用介面讓實體可以繼承其他類別（如 ValueObject）
///   
/// - 為什麼放在 Domain 層？
///   IEntity 是領域層的概念，不應該被基礎設施層污染
///   
/// - 為什麼不叫 IEntity 而叫 IAggregateRoot？
///   如果你的架構區分一般實體和聚合根，可以拆成 IEntity 和 IAggregateRoot
/// 
/// 注意事項：
/// ---------------------------------------------------------------------
/// - 這個介面不應該有任何成員，否則會強迫所有實體實作不必要的方法
/// - 不要在這個命名空間加入其他程式碼，保持 IEntity 的單純性
/// - 如果使用 Source Generator，可以配合這個介面自動產生程式碼
/// </summary>
public interface IEntity;