namespace Manian.Application.Services;

/// <summary>
/// 密碼服務介面 - 定義應用程式中處理密碼的完整生命週期
/// 
/// 這個介面封裝了三個核心功能：
/// 1. 密碼雜湊（儲存時使用）
/// 2. 密碼驗證（登入時使用）
/// 3. 隨機密碼生成（註冊或重設密碼時使用）
/// 
/// 安全設計原則：
/// - 絕不儲存或傳輸明碼密碼
/// - 使用強密碼學演算法（PBKDF2、bcrypt、Argon2）
/// - 每個密碼使用獨立的隨機鹽
/// - 提供足夠複雜的隨機密碼生成
/// </summary>
public interface IPasswordService
{
    /// <summary>
    /// 將使用者的明碼密碼進行加鹽雜湊，產生安全的密文
    /// 
    /// 流程：
    /// 1. 產生一個密碼學安全的隨機鹽（Salt）
    /// 2. 使用 PBKDF2 或其他金鑰衍生函數，將密碼和鹽進行多次迭代雜湊
    /// 3. 將鹽和雜湊結果組合成單一字串（通常用 Base64 編碼）
    /// 
    /// 為什麼需要鹽？
    /// - 防止彩虹表攻擊（預先計算好的雜湊表）
    /// - 讓相同密碼的使用者產生不同的雜湊值
    /// 
    /// 為什麼需要多次迭代？
    /// - 增加暴力破解的時間成本
    /// - 隨著硬體效能提升，迭代次數應該逐步增加
    /// 
    /// 輸出格式範例：
    /// Base64(16 bytes 鹽 + 32 bytes 雜湊) => "5L2g5aW95ZWK5a+G5ZCN56uv5Y2B5LqG..."
    /// </summary>
    /// <param name="password">使用者設定的明碼密碼</param>
    /// <returns>包含鹽和雜湊值的 Base64 密文字串，可直接儲存於資料庫</returns>
    string Hash(string password);

    /// <summary>
    /// 驗證使用者輸入的密碼是否與儲存的密文相符
    /// 
    /// 驗證流程：
    /// 1. 從密文中解析出當初使用的鹽
    /// 2. 用相同的鹽和演算法，對輸入的密碼重新計算雜湊
    /// 3. 比對重新計算的結果是否與密文中的雜湊部分相同
    /// 
    /// 這個方法應該具有恆定時間比較的特性
    /// 防止計時攻擊 (Timing Attack)
    /// 
    /// 使用時機：
    /// - 使用者登入時驗證身份
    /// - 修改密碼前驗證舊密碼
    /// - 敏感操作前的身份確認
    /// </summary>
    /// <param name="password">使用者輸入的明碼密碼</param>
    /// <param name="cipherText">資料庫中儲存的 Base64 密文</param>
    /// <returns>
    /// true: 密碼正確，允許存取
    /// false: 密碼錯誤，拒絕存取
    /// </returns>
    bool Validate(string password, string cipherText);

    /// <summary>
    /// 生成一個符合安全性要求的隨機密碼
    /// 
    /// 使用時機：
    /// - 使用者註冊時自動產生初始密碼
    /// - 「忘記密碼」功能中產生臨時密碼
    /// - 系統自動建立帳號時（如 API 金鑰）
    /// 
    /// 密碼設計考量：
    /// - 足夠的長度（預設 14 字元）
    /// - 包含大小寫字母、數字和符號（增加複雜度）
    /// - 使用密碼學安全的隨機數產生器
    /// - 避免容易混淆的字元（如 0 和 O, 1 和 l）
    /// 
    /// 注意：生成的密碼應該透過安全管道傳送給使用者
    /// 例如：加密郵件、簡訊、或要求使用者立即修改
    /// </summary>
    /// <param name="default_length">密碼長度，預設 14 字元</param>
    /// <returns>符合安全要求的隨機密碼字串</returns>
    public string Generate(int default_length = 14);
}