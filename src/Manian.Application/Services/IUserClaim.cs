namespace Manian.Application.Services;

/// <summary>
/// 當前請求的使用者身份介面
/// 
/// 這個介面提供了存取「目前登入使用者」的統一方式
/// 是 Clean Architecture 中解決「誰在做這個操作」問題的標準模式
/// 
/// 為什麼需要這個介面？
/// 1. 抽象化：不論是從 JWT、Session 還是測試環境，取得使用者 ID 的方式都一樣
/// 2. 可測試性：單元測試中可以輕易 Mock 固定的使用者 ID
/// 3. 關注點分離：業務邏輯不需要知道使用者資訊是從哪裡來的
/// 4. 程式碼意圖清晰：任何需要知道「當前使用者」的地方，都依賴這個介面
/// 
/// 使用時機：
/// - 建立資料時記錄 CreatedBy（誰建立的）
/// - 修改資料時檢查操作權限（是不是本人或有足夠權限）
/// - 查詢資料時只回傳該使用者有權限看到的內容（資料隔離）
/// - 審計日誌中記錄操作者（誰在什麼時間做了什麼）
/// - 軟刪除時記錄 DeletedBy（誰刪除的）
/// </summary>
public interface IUserClaim
{
    /// <summary>
    /// 目前登入使用者的唯一識別碼
    /// 
    /// 這個值來自認證過的請求，可能是：
    /// - JWT Token 中的 sub (subject) 宣告
    /// - Session 中儲存的使用者 ID
    /// - 測試環境中 Mock 的固定值
    /// - Windows 認證中的使用者 SID 轉換
    /// 
    /// 使用 int 型別的考量：
    /// - 資料庫主鍵常用 int，相容性高
    /// - 儲存空間較小（4 bytes vs 8 bytes）
    /// - 效能較佳（CPU 處理 32 位元整數較快）
    /// - 適合中小型系統（最大值約 21 億）
    /// 
    /// 注意：如果是大型分散式系統，可能需要改用 long
    /// 以容納 Snowflake 等演算法產生的 64 位元 ID
    /// 
    /// 使用 init 關鍵字的原因：
    /// - 確保 Id 在建構後就不能被修改
    /// - 防止在請求過程中意外變更使用者身份
    /// - 符合不可變性 (immutability) 的設計原則
    /// - 讓程式碼意圖更明確：這個值只會在建立時設定一次
    /// 
    /// 安全性保證：
    /// - 這個值應該來自已認證的使用者，不可由客戶端自行傳入
    /// - 永遠不要信任 Request Body 中的使用者 ID
    /// </summary>
    int Id { get; init; }
}